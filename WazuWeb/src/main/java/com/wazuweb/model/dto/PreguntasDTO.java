package com.wazuweb.model.dto;

public class PreguntasDTO {
	
	private int CODP;
	private String PREG;
	private int CODN;
	
	public int getCODP() {
		return CODP;
	}
	public void setCODP(int cODP) {
		CODP = cODP;
	}
	public String getPREG() {
		return PREG;
	}
	public void setPREG(String pREG) {
		PREG = pREG;
	}
	public int getCODN() {
		return CODN;
	}
	public void setCODN(int cODN) {
		CODN = cODN;
	}
	
}